/**
 * 展示Dialog
 * @param  {Function} callback 关闭回调
 * @return {[type]}
 */
ibookerEditorEmojiDailog.showDialog = function(callback) {
	/* 获取页面的宽度和高度 */
	var sHeight = document.documentElement.scrollHeight;
	var sWidth = document.documentElement.scrollWidth;

	/* 获取页面可视区域的宽度和高度，宽度和页面宽度一样 */
	var dialogMask = document.createElement("div");
	dialogMask.style.display = "block";
	dialogMask.id = "ibooker_editor_dialog_mask";
	dialogMask.style.height = sHeight + "px";
	dialogMask.style.width = sWidth + "px";
	document.body.appendChild(dialogMask);

	/* 获取浏览器的高度 */
	var wHeight = document.documentElement.clientHeight;

	// 创建弹出层内容
	var dialogContent =document.createElement("div");
	dialogContent.id = "ibooker_editor_dialog";
	dialogContent.style.display = "block";
	dialogContent.style.height = "400px";
	dialogContent.style.width = (sWidth / 2) + "px";
	// 修改弹出层内容位置 - 默认居中
	// var dHeight = dialogContent.offsetHeight;
	// var dWidth = dialogContent.offsetWidth;
	dialogContent.style.left = sWidth / 4 + "px";
	dialogContent.style.top = (wHeight - 400) / 2 + "px";
	// 添加弹出层内容
	document.body.appendChild(dialogContent);

	// 设置弹出层内容
	ibookerEditorEmojiDailog.createDialogView(dialogContent);

	// 设置关闭Dialog
	if (document.getElementById("close")) {
		document.getElementById("close").onclick = function() {
			ibookerEditorEmojiDailog.closeDialog2();
		};
	}
	// 设置点击遮盖层，关闭Dialog
	if (dialogMask) {
		dialogMask.onclick = function() {
			ibookerEditorEmojiDailog.closeDialog2();
		};
	}
	// 赋值
	ibookerEditorEmojiDailog.closeEmojiDialogCallback = callback;
};

// 关闭Dialog1
ibookerEditorEmojiDailog.closeDialog2 = function () {
	var dialogMask = document.getElementById("ibooker_editor_dialog_mask");
	document.body.removeChild(dialogMask);

	var dialogContent = document.getElementById("ibooker_editor_dialog");
	document.body.removeChild(dialogContent);
};

// 关闭Dialog2 - 有关闭回调函数
ibookerEditorEmojiDailog.closeDialog = function (selectTxt) {
	ibookerEditorEmojiDailog.closeDialog2();
	if (ibookerEditorEmojiDailog.closeEmojiDialogCallback && typeof ibookerEditorEmojiDailog.closeEmojiDialogCallback === "function"){
        ibookerEditorEmojiDailog.closeEmojiDialogCallback(selectTxt); 
    }
};

